public class ControlFlowExercises {
    public static void main(String[] args) {
        int i = 5;
        while (i <= 15) {
            System.out.println(i);
            i++;
        }
//        DO while loop to count by two, till the counter hits 100
        int i1 = 2;
        do {
            System.out.println(i1);
            i1 += 2;
        } while (i1 <= 100);
        i1 = i1 + 2;







    }
}